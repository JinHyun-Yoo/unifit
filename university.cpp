int uniCount = sizeof(uniList) / sizeof(uniList[0]);

/*
    대학 입시 예상 및 지원 함수
    함수명: is_verse()
    설명: 학생의 내신 등급과 대학별 기준 등급을 비교하여 지원 가능성을 판단한다.
*/
void is_verse(Student* student) {
    if (student == NULL) {
        printf("학생을 찾을 수 없습니다.\n");
        return;
    }

    printf("\n========== 대학 입시 예상 및 지원 ==========" "\n");
    printf("학생 이름: %s\n", student->name);
    printf("학생 평균: %.2lf\n", student->average);
    printf("학생 내신 등급: %.1lf\n", student->grade);
    printf("==========================================\n");

    for (int i = 0; i < uniCount; i++) {
        double diff = uniList[i].cutline - student->grade;

        printf("\n대학교: %s\n", uniList[i].name);
        printf("학과: %s\n", uniList[i].major);
        printf("전형: %s\n", uniList[i].type);
        printf("기준 내신 등급: %.1lf\n", uniList[i].cutline);

        if (diff >= 0.5) {
            printf("결과: 안정 지원\n");
            printf("조언: 합격 가능성이 비교적 높습니다. 면접과 자기소개 준비를 함께 하면 좋습니다.\n");
        }
        else if (diff >= 0.0) {
            printf("결과: 적정 지원\n");
            printf("조언: 현재 성적으로 지원을 고려할 수 있습니다. 약한 과목을 보완하세요.\n");
        }
        else if (diff >= -0.5) {
            printf("결과: 상향 지원\n");
            printf("조언: 도전 지원입니다. 성적 향상이나 면접 준비가 중요합니다.\n");
        }
        else {
            printf("결과: 지원 어려움\n");
            printf("조언: 현재 성적으로는 어려울 수 있습니다. 다른 대학이나 전형도 함께 확인하세요.\n");
        }
    }

    printf("\n※ 위 입시 기준은 프로그램 예시용 데이터입니다.\n");
}

