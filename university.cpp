/* 회원 내신과 대학 기준 내신을 비교해 결과를 출력하는 보조 함수 */
static void printGradeResult(double memberGrade, double cutline) {
    double diff = cutline - memberGrade;

    printf("회원 내신 등급: %.2lf\n", memberGrade);
    printf("기준 내신 등급: %.2lf\n", cutline);

    if (diff >= 0.50) {
        printf("결과: 안정 지원\n");
        printf("조언: 현재 내신 기준으로는 여유가 있는 편입니다. 면접과 서류 준비도 함께 하세요.\n");
    }
    else if (diff >= 0.00) {
        printf("결과: 적정 지원\n");
        printf("조언: 지원을 고려할 수 있습니다. 자기소개, 면접, 세부 활동 정리가 중요합니다.\n");
    }
    else if (diff >= -0.50) {
        printf("결과: 상향 지원\n");
        printf("조언: 도전 지원입니다. 전형 요소 중 강점을 살리는 전략이 필요합니다.\n");
    }
    else {
        printf("결과: 지원 어려움\n");
        printf("조언: 다른 대학, 다른 전형, 비슷한 계열 학과도 함께 비교해 보세요.\n");
    }
}

